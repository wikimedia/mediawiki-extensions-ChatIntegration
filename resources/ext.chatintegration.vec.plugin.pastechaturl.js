ext = ext || {}; // eslint-disable-line no-global-assign, no-implicit-globals
ext.chatintegration = ext.chatintegration || {};
ext.chatintegration.dataTransferHandler = ext.chatintegration.dataTransferHandler || {};

const config = require( './config.json' );

ext.chatintegration.dataTransferHandler.PasteChatUrlHandler = function ChatIntegrationPasteChatUrlHandler() {
	ext.chatintegration.dataTransferHandler.PasteChatUrlHandler.super.apply( this, arguments );
};

OO.inheritClass( ext.chatintegration.dataTransferHandler.PasteChatUrlHandler, ve.ui.UrlStringTransferHandler );

ext.chatintegration.dataTransferHandler.PasteChatUrlHandler.static.name = 'pasteChatUrl';
ext.chatintegration.dataTransferHandler.PasteChatUrlHandler.static.handlesPaste = true;

ext.chatintegration.dataTransferHandler.PasteChatUrlHandler.static.matchFunction = function ( item ) {
	const url = ext.chatintegration.dataTransferHandler.PasteChatUrlHandler.static.extractPastableUrl( item );

	if ( !url ) {
		return false;
	}

	if ( Object.values( config.tagToBaseUrls ).some( ( prefix ) => prefix && url.startsWith( prefix ) ) ) {
		return true;
	}

	return false;
};

ext.chatintegration.dataTransferHandler.PasteChatUrlHandler.prototype.process = function () {
	const url = ext.chatintegration.dataTransferHandler.PasteChatUrlHandler.static.extractPastableUrl( this.item );
	let channel = null;
	let integration = null;

	const failure = () => {
		this.resolve( url );
	};

	for ( const [ key, baseUrl ] of Object.entries( config.tagToBaseUrls ) ) {
		if ( url.startsWith( baseUrl ) ) {
			integration = key;
			channel = url.slice( baseUrl.length );
			break;
		}
	}

	if ( !integration || !channel ) {
		return failure();
	}

	const tag = `<${ integration } url="${ url }"/>`;

	this.parsoidRequest = ve.init.target.parseWikitextFragment( tag, false, this.surface.getModel().getDocument() );

	this.parsoidRequest.then( ( response ) => {
		if ( response.visualeditor.result !== 'success' ) {
			return failure();
		}

		const doc = ext.chatintegration.dataTransferHandler.PasteChatUrlHandler.static.createDocumentFromParsoidHtml(
			response.visualeditor.content,
			this.surface.getModel().getDocument()
		);

		if ( !doc.data.hasContent() ) {
			return failure();
		}

		this.resolve( doc );
	}, failure );

	this.createProgress( this.parsoidRequest, ve.msg( 'visualeditor-wikitext-progress' ) );
	this.setProgress( null );
};

ext.chatintegration.dataTransferHandler.PasteChatUrlHandler.static.createDocumentFromParsoidHtml = function ( html, targetDoc ) {
	const htmlDoc = ve.createDocumentFromHtml( html );

	// Strip RESTBase IDs
	mw.libs.ve.stripRestbaseIds( htmlDoc );

	// Strip legacy IDs, for example in section headings
	mw.libs.ve.stripParsoidFallbackIds( htmlDoc.body );

	// Pass an empty object for the second argument (importRules) so that clipboard mode is used
	const doc = targetDoc.newFromHtml( htmlDoc, {} );
	const data = doc.data.data;
	const surface = new ve.dm.Surface( doc );

	// Filter out auto-generated items, e.g. reference lists
	// This is done after conversion as the autoGenerated item may contain data
	// required by other non-autoGenerated items, e.g. reference contents
	for ( let i = data.length - 1; i >= 0; i-- ) {
		if ( ve.getProp( data[ i ], 'attributes', 'mw', 'autoGenerated' ) ) {
			surface.change(
				ve.dm.TransactionBuilder.static.newFromRemoval(
					doc,
					surface.getDocument().getDocumentNode().getNodeFromOffset( i + 1 ).getOuterRange()
				)
			);
		}
	}

	// Clone elements to avoid about attribute conflicts (T204007)
	doc.data.cloneElements( true );

	return doc;
};

ext.chatintegration.dataTransferHandler.PasteChatUrlHandler.static.extractPastableUrl = function ( item ) {
	const url = item.getAsString().trim();

	// Check if it's a plain URL
	if ( /^https?:\/\//i.test( url ) ) {
		return url;
	}

	const parser = new DOMParser();
	const doc = parser.parseFromString( url, 'text/html' );
	const href = doc.querySelector( 'a' )?.getAttribute( 'href' ) || '';

	return href;
};

ve.ui.dataTransferHandlerFactory.register( ext.chatintegration.dataTransferHandler.PasteChatUrlHandler );
